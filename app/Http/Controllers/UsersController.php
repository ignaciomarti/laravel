     * @param  \Illuminate\Http\Request  $request
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        $request->validate([
            'name' => 'required|max:50',
            'email' => 'email:rfc,dns|required',
            'password' => 'nullable|confirmed',
            'password_confirmation' => Rule::requiredIf($request->password != null)
        ]);
        $request->whenFilled('password', function($user, $request){
            $user->update($request->all());
            return redirect()->route('home')->with(['success' => 'The your user information has been updated successfully']);
        });
        $request->update($request->only('name', 'email'));
        return redirect()->route('home')->with(['success' => 'The your user information has been updated successfully']);
    }

